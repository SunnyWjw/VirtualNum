+ (BOOL) strIsNullOrEmpty:(NSString *)string{
    if (![string isEqual:[NSNull null]] && ![string isEqual:nil] && string.length >0 &&  ![string isEqualToString:@"(null)"] && ![string isKindOfClass:[NSNull class]]) {
        return YES;
    }
    return NO;
}

